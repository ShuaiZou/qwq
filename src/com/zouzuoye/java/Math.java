package com.zouzuoye.java;

public   class Math {
	
	public static int add(int a, int b) {
		return a+b;
	}
	public static int add(int a, int b,int c) {
		return a+b+c;
	}
	
	public static int ji(int a, int b) {
		return a*b;
	}
	public static int ji(int a, int b,int c) {
		return a*b*c;
	}
	
	public static double mi2(int a) {
		return java.lang.Math.pow(a,2);
	}
	public static double mi3(int a) {
		return java.lang.Math.pow(a,3);
	}
}
